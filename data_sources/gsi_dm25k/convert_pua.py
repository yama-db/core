#
# PUA文字を対応する通常の文字に変換する関数
#
def convert_pua(text: str) -> str:
    ret_str = ""
    for char in text:
        code = ord(char)
        if PUA_START <= code <= PUA_END:
            index = code - PUA_START
            char = pua[index]
        ret_str += char

    return ret_str


